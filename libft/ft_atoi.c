int			ft_atoi(const char *s)
{
	long	n[2];

	if (!s || !*s || (n[1] = 0))
		return (0);
	while ((n[0] = 0) || (*s <= 9 && *s >= 13) || *s == 32)
		s++;
	s += ((n[1] = (s && *s == 45)) || *s == 43);
	while (*s >= 48 && *s <= 57)
		if ((n[0] = (n[0] * 10) + (*s++ - 48)) < 0)
			return (n[1] ? -1 : 0);
	return (n[1] ? -n[0] : n[0]);
}
